  __HAL_RCC_GPIOB_CLK_ENABLE();

  /*Configure GPIO pin Output Level */
  HAL_GPIO_WritePin(GPIOA, INP_1_Pin|RUN_LED_Pin, GPIO_PIN_RESET);

  /*Configure GPIO pin Output Level */
  HAL_GPIO_WritePin(GPIOB, INP_2_Pin|INP_3_Pin|INP_4_Pin|INP_5_Pin
                          |PWEN_1_Pin|PWEN_2_Pin|PWEN_3_Pin|PWEN_4_Pin
                          |PWEN_5_Pin, GPIO_PIN_RESET);

  /*Configure GPIO pins : PAPin PAPin */
